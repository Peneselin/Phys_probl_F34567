print("""16.14. На скільки змінилась кінетична енергія електрона в атомі водню 
при випромінюванні атомом фотона з довжиною хвилі λ нм?""")
delta_lambda = float(input("Введіть значчення довжиною хвилі фотона(в нм): ")) / (10 ** 9)
e = 6.625 / (10 ** 34) / delta_lambda * 3 * (10 ** 8)
i = 0
while e < 1:
    i += 1
    e *= 10

print(f"Кінетична енергія електрона в атомі водню при випромінюванні атомом фотона зміниться на {round(e, 4)}{10**(-i)} Дж.")
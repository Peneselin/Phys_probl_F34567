//  13.10. На поверхню скляного об’єктива з показником заломлення 1,5 нанесено тонку плівку з показником заломлення 1,2. При якій найменшій товщині цієї плівки відбите світло буде максимально послаблене для довжини хвилі 550 нм?

#include <iostream>
#include <clocale>
using namespace std;

int main()
{
    setlocale(LC_CTYPE, "ukr");

    // n1 тепер константа
    const double n1 = 1.5;
    double n2, lambda;

    cout << "Розрахунок для скляного об'єктива з показником заломлення n1 = 1.5" << endl;

    cout << "Введіть показник заломлення плівки n2 (строго менше ніж 1.5): ";
    cin >> n2;

    // Валідація для n2 
    if (n2 <= 1.0 || n2 >= n1) {
        cout << "Показник плівки n2 має бути більшим за 1 і строго меншим за 1.5!\n";
        system("pause");
        return 1;
    }

    cout << "Введіть довжину хвилі lambda (нм): ";
    cin >> lambda;

    if (lambda <= 0) {
        cout << "Довжина хвилі повинна бути додатною, а не від'ємною\n";
        system("pause");
        return 1;
    }

    // Переведення довжини хвилі з нанометрів у метри
    double lambda_m = lambda / 1e9;

    // Порядок інтерференції для найменшої товщини плівки m = 0
    double m = 0.0;

    // Умова мінімуму світла 
    double delta_min = (2.0 * m + 1.0) * (lambda_m / 2.0);

    // Розрахунок товщини плівки з оптичної різниці ходу
    double d = delta_min / (2.0 * n2);

    // Переведення результату назад у нанометри
    double d_nm = d * 1e9;

    cout << "Найменша товщина плівки d = " << d_nm << " нм" << endl;

    system("pause");
    return 0;
}
